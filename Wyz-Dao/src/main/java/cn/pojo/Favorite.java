package cn.pojo;


public class Favorite {

  private long fid;
  private long fname;
  private String fcontent;
  private String f1;
  private String f2;
  private String f3;


  public long getFid() {
    return fid;
  }

  public void setFid(long fid) {
    this.fid = fid;
  }


  public long getFname() {
    return fname;
  }

  public void setFname(long fname) {
    this.fname = fname;
  }


  public String getFcontent() {
    return fcontent;
  }

  public void setFcontent(String fcontent) {
    this.fcontent = fcontent;
  }


  public String getF1() {
    return f1;
  }

  public void setF1(String f1) {
    this.f1 = f1;
  }


  public String getF2() {
    return f2;
  }

  public void setF2(String f2) {
    this.f2 = f2;
  }


  public String getF3() {
    return f3;
  }

  public void setF3(String f3) {
    this.f3 = f3;
  }

}
