package cn.pojo;


public class Order {

    private Integer oid;
    private Integer oname;
    private Integer ocommodity;
    private Integer ostate;
    private Integer onumber;
    private Integer omoney;
    private String o1;
    private String o2;
    private String o3;

    public Integer getOid() {
        return oid;
    }

    public void setOid(Integer oid) {
        this.oid = oid;
    }

    public Integer getOname() {
        return oname;
    }

    public void setOname(Integer oname) {
        this.oname = oname;
    }

    public Integer getOcommodity() {
        return ocommodity;
    }

    public void setOcommodity(Integer ocommodity) {
        this.ocommodity = ocommodity;
    }

    public Integer getOstate() {
        return ostate;
    }

    public void setOstate(Integer ostate) {
        this.ostate = ostate;
    }

    public Integer getOnumber() {
        return onumber;
    }

    public void setOnumber(Integer onumber) {
        this.onumber = onumber;
    }

    public Integer getOmoney() {
        return omoney;
    }

    public void setOmoney(Integer omoney) {
        this.omoney = omoney;
    }

    public String getO1() {
        return o1;
    }

    public void setO1(String o1) {
        this.o1 = o1;
    }

    public String getO2() {
        return o2;
    }

    public void setO2(String o2) {
        this.o2 = o2;
    }

    public String getO3() {
        return o3;
    }

    public void setO3(String o3) {
        this.o3 = o3;
    }
}
